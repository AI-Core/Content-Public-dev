while [[ "$MACHINE_IP" == "$ip" ]]
  do
    export ip=$(aws ec2 describe-instances | grep -E -o "(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)" | tail -2 | head -1)
  done
echo $MACHINE_IP
